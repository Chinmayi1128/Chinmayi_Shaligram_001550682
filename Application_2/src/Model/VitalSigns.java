public class VitalSigns {
    
    private float Age;
    private int Respiratory_Rate;
    private int Heart_Rate;
    private int Blood_Pressure;
    private double Wt_Kg;
    private double Wt_Pound;

    public float getAge() {
        return Age;
    }

    public void setAge(float Age) {
        this.Age = Age;
    }

    public int getRespiratory_Rate() {
        return Respiratory_Rate;
    }

    public void setRespiratory_Rate(int Respiratory_Rate) {
        this.Respiratory_Rate = Respiratory_Rate;
    }

    public int getHeart_Rate() {
        return Heart_Rate;
    }

    public void setHeart_Rate(int Heart_Rate) {
        this.Heart_Rate = Heart_Rate;
    }

    public int getBlood_Pressure() {
        return Blood_Pressure;
    }

    public void setBlood_Pressure(int Blood_Pressure) {
        this.Blood_Pressure = Blood_Pressure;
    }

    public double getWt_Kg() {
        return Wt_Kg;
    }

    public void setWt_Kg(double Wt_Kg) {
        this.Wt_Kg = Wt_Kg;
    }

    public double getWt_Pound() {
        return Wt_Pound;
    }

    public void setWt_Pound(double Wt_Pound) {
        this.Wt_Pound = Wt_Pound;
    }
   
    
}

