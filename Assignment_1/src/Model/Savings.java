public class Savings {
    private String Bank_Name;
    private String Bank_Routing;
    private String Account_No;
    private String Bank_Balance;
    private String Account_Type;

    public String getBank_Name() {
        return Bank_Name;
    }

    public void setBank_Name(String Bank_Name) {
        this.Bank_Name = Bank_Name;
    }

    public String getBank_Routing() {
        return Bank_Routing;
    }

    public void setBank_Routing(String Bank_Routing) {
        this.Bank_Routing = Bank_Routing;
    }

    public String getAccount_No() {
        return Account_No;
    }

    public void setAccount_No(String Account_No) {
        this.Account_No = Account_No;
    }

    public String getBank_Balance() {
        return Bank_Balance;
    }

    public void setBank_Balance(String Bank_Balance) {
        this.Bank_Balance = Bank_Balance;
    }

    public String getAccount_Type() {
        return Account_Type;
    }

    public void setAccount_Type(String Account_Type) {
        this.Account_Type = Account_Type;
    }
    
    
}
