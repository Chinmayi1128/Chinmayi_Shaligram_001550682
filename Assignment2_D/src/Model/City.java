package Model;

/**
 *
 * @author chinmayi_shaligram
 */
public class City {
    private String City;
    private String State;
    
    public City(String City, String State){
        this.City = City;
        this.State = State;
    }

    public String getCity() {
        return City;
    }

    public void setCity(String City) {
        this.City = City;
    }

    public String getState() {
        return State;
    }

    public void setState(String State) {
        this.State = State;
    }
    
    
}
